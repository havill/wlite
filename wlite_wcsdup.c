#include <stdlib.h>         // malloc() / FIXME: freestanding

#include "wlite_config.h"   // wchar_t, NULL, size_t

#include "wlite_wchar.h"    // prototypes
#include "wlite_wctype.h"
#include "wlite_stdlib.h"

wchar_t *
wlite_wcsdup(const wchar_t *s) {
    wchar_t *dup;

    dup = malloc(sizeof(wchar_t) * (wlite_wcslen(s) + 1));
    if (dup != NULL) {
        wlite_wcscpy(dup, s);
    }
    return dup;
}
